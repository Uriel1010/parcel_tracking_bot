def normalize_locale(locale: str | None) -> str:
    if not locale:
        return DEFAULT_LOCALE
    short = locale.split("-")[0].lower()
    return short if short in SUPPORTED_LOCALES else DEFAULT_LOCALE


def t(locale: str | None, key: str, **params: Any) -> str:
    normalized = normalize_locale(locale)
    template = TRANSLATIONS.get(normalized, {}).get(key) or TRANSLATIONS[DEFAULT_LOCALE].get(key) or key
    return template.format(**params)


def status_label(locale: str | None, status_code: str) -> str:
    normalized = normalize_locale(locale)
    return STATUS_LABELS.get(normalized, {}).get(status_code, STATUS_LABELS[DEFAULT_LOCALE].get(status_code, status_code))
